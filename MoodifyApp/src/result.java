     public result(String t) {
         top=t;
        initComponents();
         String Api_url="https://sentimentapi01.herokuapp.com/topic/".concat(top);
        try{
            URL url=new URL(Api_url);
            HttpURLConnection huc=(HttpURLConnection)url.openConnection();
            BufferedReader in= new BufferedReader(new InputStreamReader(huc.getInputStream()));
            String inputline;
            StringBuffer response = new StringBuffer();
            while((inputline=in.readLine())!=null){
                response.append(inputline);
            }
            extract et=new extract();

            PossitiveRes=Double.parseDouble(et.extractdata(response.toString(),"PossitiveTweets"));
            NegativeRes=Double.parseDouble(et.extractdata(response.toString(),"NegativeTweets"));
            NeutralRes=Double.parseDouble(et.extractdata(response.toString(),"NeutralTweets"));

            compare c=new compare();
            String conclusion=c.compareDoubleValues(PossitiveRes,NegativeRes,NeutralRes);
            jLabel1.setText("The opinion of majority of twitter community on "+top+" is "+conclusion);
            //t2.setText(response.toString());
            huc.disconnect();
        }
        catch(Exception e2){System.out.println(e2);}
    }
